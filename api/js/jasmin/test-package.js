// 需要一个 mocking 库进行测试时使用。

// ESdoc：
// 你在使用较新版本的 JS，想从代码中生成 API 文档时用它。
// 支持当前版本的 JS，默认目标 class。所以如果你在代码中使用原型，请使用 JSdoc。

// JSdoc：
// 你需要支持 ES6 的代码 API 文档生成器时用它。
// 支持类和原型。

